package newproject1.Domain;

public class Boreple {

  private int no;
  private int bono;
  private String note;
  private String rgdt;
  private String stat;
  
  public int getNo() {
    return no;
  }
  public void setReno(int no) {
    this.no = no;
  }
  public int getBono() {
    return bono;
  }
  public void setBono(int bono) {
    this.bono = bono;
  }
  public String getNote() {
    return note;
  }
  public void setNote(String note) {
    this.note = note;
  }
  public String getRgdt() {
    return rgdt;
  }
  public void setRgdt(String rgdt) {
    this.rgdt = rgdt;
  }
  public String getStat() {
    return stat;
  }
  public void setStat(String stat) {
    this.stat = stat;
  }
  
}
